# operadores aritmeticos
suma = 10 + 3
print("Suma: 10 + 3 =")
print(suma)
resta = 10 - 3
print("Resta: 10 - 3 =")
print(resta)
multiplicacion = 10 * 3
print("Multiplicacion: 10 * 3 =")
print(multiplicacion)
divison = 10 / 3
print("Divison: 10 / 3 =")
print(divison)
modulo = 10 % 3
print("Mood: 10 % 3 =")
print(modulo)
exponente = 10 ** 3
print("Exponente: 10 ** 3 =")
print(exponente)
division_entera = 10 // 3
print("Division entera: 10 // 3 =")
print(division_entera)
print("\n")
#________________________________________________________________________________________
# Interpolacion en cadena de texto operadores aritmeticos
print(f"Suma: 10 + 3 = {10 + 3}")  
print(f"Resta: 10 - 3 = {10 - 3}")
print(f"Multiplicacion: 10 * 3 = {10 * 3}")
print(f"Division: 10 / 3 = {10 / 3}")
print(f"Modulo: 10 % 3 = {10 % 3}")
print(f"Exponente: 10 ** 3 = {10 ** 3}")
print(f"Division entera: 10 // 3 = {10 // 3}\n")

